/**
 * Main Installer App Component
 * Enforces API key validation before allowing MCP client installation
 */
const InstallerApp: React.FC = () => {
  const [step, setStep] = useState<InstallationStep>('welcome');
  const [apiKey, setApiKey] = useState('');
  const [selectedClients, setSelectedClients] = useState<string[]>([]);
  const [results, setResults] = useState<InstallationResult[]>([]);
  const [error, setError] = useState('');
  const { exit } = useApp();

  const renderStep = () => {
    switch (step) {
      case 'welcome':
        return <WelcomeScreen onNext={() => setStep('apiKey')} />;

      case 'apiKey':
        return (
          <ApiKeyScreen
            onNext={(key) => {
              setApiKey(key);
              setStep('clientSelection');
            }}
            onBack={() => setStep('welcome')}
          />
        );

      case 'clientSelection':
        return (
          <ClientSelectionScreen
            onNext={(clients) => {
              setSelectedClients(clients);
              setStep('installing');
            }}
            onBack={() => setStep('apiKey')}
          />
        );

      case 'installing':
        return (
          <InstallationProgressScreen
            apiKey={apiKey}
            selectedClients={selectedClients}
            onComplete={(res) => {
              setResults(res);
              setStep('complete');
            }}
            onError={(err) => {
              setError(err);
              setStep('error');
            }}
          />
        );

      case 'complete':
        return <InstallationCompleteScreen results={results} onExit={() => exit()} />;

      case 'error':
        return (
          <Box flexDirection="column" padding={1}>
            <Text color="cyan" bold>
              üöÄ SmartLead MCP Server - Interactive Installer
            </Text>
            <Text></Text>
            <Text color="red" bold>
              ‚ùå An Error Occurred
            </Text>
            <Text></Text>
            <Text color="red">{error}</Text>
            <Text></Text>
            <Text color="yellow">üí° Troubleshooting:</Text>
            <Text>‚Ä¢ Check your API key is valid.</Text>
            <Text>‚Ä¢ Ensure you have write permissions.</Text>
            <Text>‚Ä¢ Try running with administrator privileges.</Text>
            <Text>
              ‚Ä¢ Visit: <Link url="https://github.com/LeadMagic/smartlead-mcp-server">GitHub</Link>
            </Text>
            <Text></Text>
            <Text dimColor>Press R to restart, or ESC to exit.</Text>
          </Box>
        );

      default:
        return (
          <Box>
            <Spinner type="dots" />
            <Text> Loading...</Text>
          </Box>
        );
    }
  };

  useInput((input, key) => {
    if (step === 'error') {
      if (key.escape || input === 'q') exit();
      if (input === 'r' || key.return) {
        setStep('welcome');
        setApiKey('');
        setSelectedClients([]);
        setResults([]);
        setError('');
      }
    }
  });

  return renderStep();
};

// Handle graceful shutdown
process.on('SIGINT', () => {
  console.log('\n\nüëã Installation cancelled. Run the installer again anytime!');
  process.exit(0);
});

process.on('SIGTERM', () => {
  console.log('\n\nüëã Installation terminated.');
  process.exit(0);
});

// Render the app if this file is executed directly
if (import.meta.url === `file://${process.argv[1]}`) {
  render(<InstallerApp />);
}

export default InstallerApp;
